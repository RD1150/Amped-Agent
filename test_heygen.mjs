// Test HeyGen API connectivity and key validity
const key = process.env.HEYGEN_API_KEY;
if (!key) { console.error("HEYGEN_API_KEY not set"); process.exit(1); }
console.log("Key prefix:", key.substring(0, 8) + "...");

// Test 1: List avatars (basic connectivity test)
console.log("\n--- Test 1: List avatars ---");
const r1 = await fetch("https://api.heygen.com/v2/avatars", {
  headers: { "X-Api-Key": key, accept: "application/json" }
});
console.log("Status:", r1.status);
const d1 = await r1.json();
console.log("Response:", JSON.stringify(d1).substring(0, 200));

